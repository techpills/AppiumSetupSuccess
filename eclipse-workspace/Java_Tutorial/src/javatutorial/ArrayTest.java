package javatutorial;

public class ArrayTest {

public static void main(String args[])
{
	int [] numbers = {2, 4, 6, 8};
	
	for(int x : numbers)
	{
		System.out.println(x);
	}
	
	String [] names = {"viknesh", "waran"};
	
	for(String name : names)
	{
		System.out.print(name);
		System.out.print(",");
	}
	
	
	
}
	
	
}
